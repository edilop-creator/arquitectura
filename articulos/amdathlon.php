        <h1>Procesadores AMD Athlon – Especificaciones</h1>

        <p>La serie <strong>AMD Athlon</strong> es una de las familias de procesadores más antiguas y emblemáticas de AMD.  
        Desde su aparición en 1999, ha pasado por múltiples generaciones, desde los primeros procesadores de un solo núcleo  
        hasta las versiones modernas basadas en la arquitectura <strong>Zen</strong>.  
        Estos procesadores están diseñados principalmente para equipos de gama básica y media, ofreciendo una excelente relación calidad-precio.</p>

        <hr>

        <h2>Características generales de la serie AMD Athlon</h2>
        <ul>
            <li><strong>Uso principal:</strong> Equipos de oficina, navegación web, multimedia y tareas cotidianas.</li>
            <li><strong>Arquitectura moderna:</strong> Zen / Zen+ / Excavator (según el modelo).</li>
            <li><strong>Litografía:</strong> 14 nm o 12 nm (modelos modernos).</li>
            <li><strong>Soporte de memoria:</strong> DDR4 (hasta 2933 o 3200 MHz en modelos nuevos).</li>
            <li><strong>Socket:</strong> AM4 (modelos recientes) y FM2/FM2+ (anteriores).</li>
            <li><strong>Gráficos integrados:</strong> Radeon Vega (en modelos con GPU integrada).</li>
            <li><strong>Overclocking:</strong> Solo disponible en versiones con la letra “G” o “GE”.</li>
        </ul>

