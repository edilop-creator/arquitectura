        <h2>Ventajas y desventajas</h2>

        <h3>Ventajas:</h3>
        <ul>
            <li>Excelente relación precio/rendimiento.</li>
            <li>Consumo energético bajo (TDP de 35W a 65W).</li>
            <li>Modelos con gráficos Radeon Vega integrados.</li>
            <li>Compatibles con placas base AM4 modernas.</li>
