        </ul>

        <h3>Desventajas:</h3>
        <ul>
            <li>Rendimiento limitado en tareas exigentes o juegos modernos.</li>
            <li>Menor cantidad de núcleos frente a Ryzen.</li>
            <li>Gráficos Vega integrados básicos.</li>
        </ul>

