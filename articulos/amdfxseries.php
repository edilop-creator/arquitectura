        <h1>Procesadores AMD FX Series – Especificaciones</h1>

        <p>La serie <strong>AMD FX</strong> fue una línea de procesadores lanzada por AMD para equipos de escritorio de alto rendimiento.  
        Basada en la arquitectura <strong>Bulldozer</strong> y sus revisiones (Piledriver, Steamroller y Excavator), la familia FX ofrecía múltiples núcleos físicos, frecuencias elevadas y soporte para overclocking sin restricciones.</p>

        <hr>

        <h2>Características generales de la serie AMD FX</h2>
        <ul>
            <li><strong>Socket:</strong> AM3+</li>
            <li><strong>Compatibilidad de memoria:</strong> DDR3 hasta 2133 MHz (OC)</li>
            <li><strong>Proceso de fabricación:</strong> 32 nm SOI</li>
            <li><strong>Soporte de overclocking:</strong> Sí (desbloqueados de fábrica)</li>
            <li><strong>Arquitecturas utilizadas:</strong> Bulldozer, Piledriver, Steamroller, Excavator</li>
            <li><strong>Lanzamiento inicial:</strong> 2011</li>
            <li><strong>Últimos modelos lanzados:</strong> 2017</li>
        </ul>

