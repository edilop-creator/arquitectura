        <h1>Fedora Linux: La Plataforma Innovadora de Código Abierto</h1>
        <hr>

        <h2>1. Definición y Propósito</h2>
        <p>
            Fedora es una distribución del sistema operativo GNU/Linux que sirve como base para la comunidad de Red Hat. Está enfocada en la <strong>innovación, la integración de las últimas tecnologías y el software libre y de código abierto (FOSS)</strong>.
        </p>
        <ul>
            <li><strong>Patrocinador Principal:</strong> Red Hat (ahora parte de IBM).</li>
            <li><strong>Filosofía:</strong> Ser un "campo de pruebas" (upstream) para las tecnologías que más tarde podrían ser integradas en Red Hat Enterprise Linux (RHEL).</li>
        </ul>

        <h2>2. Características Clave</h2>
        <p>Fedora se distingue por ser una distribución de vanguardia:</p>
        <ol>
            <li><strong>Innovación Rápida:</strong> Tiende a incluir las últimas versiones del kernel Linux, entornos de escritorio y aplicaciones, mucho antes que otras distribuciones más conservadoras.</li>
            <li><strong>Uso de GNOME por Defecto:</strong> La edición principal (Fedora Workstation) utiliza el entorno de escritorio GNOME con una experiencia casi pura.</li>
            <li><strong>Seguridad Fuerte:</strong> Es pionero en el uso de tecnologías de seguridad como **SELinux (Security-Enhanced Linux)**, el cual viene activado y configurado por defecto.</li>
            <li><strong>Código 100% Abierto:</strong> Mantiene una política estricta de solo incluir software libre en sus repositorios principales, aunque permite repositorios de terceros para software propietario.</li>
        </ol>

        <h2>3. Ediciones Principales</h2>
        <p>Fedora se presenta en varias ediciones adaptadas a diferentes necesidades:</p>
        <ul>
            <li><strong>Fedora Workstation:</strong> Orientada a usuarios de escritorio, programadores y entusiastas.</li>
            <li><strong>Fedora Server:</strong> Para servidores, enfocada en la estabilidad, la gestión remota y las tecnologías de servidor más recientes.</li>
            <li><strong>Fedora IoT:</strong> Diseñada para la Internet de las Cosas y dispositivos embebidos.</li>
            <li><strong>Fedora Spins:</strong> Versiones alternativas de escritorio que usan entornos como KDE Plasma, XFCE, o Cinnamon.</li>
        </ul>

        <h2>4. Ciclo de Vida</h2>
        <p>
            Fedora tiene un ciclo de lanzamiento relativamente corto, publicando una nueva versión aproximadamente cada seis meses. Cada versión tiene un soporte de alrededor de 13 meses, lo que fomenta que los usuarios se mantengan actualizados.
        </p>

