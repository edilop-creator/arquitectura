        <h1>Linux: El Sistema Operativo de Código Abierto</h1>
        <hr>

        <h2>1. Definición Básica</h2>
        <p>
            Linux es un <strong>núcleo (kernel)</strong> de sistema operativo libre y de código abierto.
            A menudo, cuando la gente habla de "Linux", se refiere al sistema operativo completo formado por el núcleo Linux más las utilidades del proyecto GNU. Por ello, técnicamente se le conoce como <strong>GNU/Linux</strong>.
        </p>

        <h2>2. Origen</h2>
        <ul>
            <li><strong>Creador del Kernel:</strong> Linus Torvalds, quien lo desarrolló y lanzó por primera vez en 1991.</li>
            <li><strong>Filosofía:</strong> Está basado en los principios de <strong>código abierto (Open Source)</strong>. Esto significa que su código fuente es público, y cualquiera puede verlo, modificarlo y distribuirlo.</li>
        </ul>

        <h2>3. Características Clave</h2>
        <p>Linux destaca por su flexibilidad y poder:</p>
        <ol>
            <li><strong>Distribuciones (Distros):</strong> No existe una única versión de Linux. Hay cientos de "distribuciones" o versiones personalizadas que empaquetan el kernel y el software de manera diferente (ej: Ubuntu, Fedora, Debian, Mint, Kali).</li>
            <li><strong>Multitarea y Multiusuario:</strong> Está diseñado para ejecutar múltiples programas y ser utilizado por múltiples usuarios a la vez, con gran estabilidad.</li>
            <li><strong>Seguridad:</strong> Es conocido por su arquitectura de seguridad robusta y su rápido ciclo de corrección de errores por parte de la comunidad.</li>
            <li><strong>Personalización:</strong> Los usuarios tienen control total sobre el sistema y pueden elegir su propio <strong>Entorno de Escritorio</strong> (como GNOME o KDE) y herramientas.</li>
        </ol>

        <h2>4. Usos Comunes</h2>
        <p>Aunque muchas personas lo usan en su PC (escritorio), Linux domina otros campos:</p>
        <ul>
            <li><strong>Servidores Web:</strong> La mayoría de los sitios web en el mundo (incluyendo Google, Facebook, etc.) se ejecutan en servidores Linux.</li>
            <li><strong>Dispositivos Móviles:</strong> Android está construido sobre el núcleo Linux.</li>
            <li><strong>Supercomputadoras:</strong> Prácticamente todas las supercomputadoras del mundo usan Linux.</li>
            <li><strong>Dispositivos Embebidos:</strong> Se encuentra en routers, grabadoras de video y muchos dispositivos IoT.</li>
        </ul>
