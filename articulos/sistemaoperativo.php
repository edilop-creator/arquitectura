        <h1>Sistemas Operativos: Conceptos Fundamentales</h1>

        <h2>1. Definición y Función Principal</h2>
        <p><strong>Definición:</strong> Un Sistema Operativo (OS) es el software principal o conjunto de programas que gestiona los recursos de hardware y proporciona servicios comunes para los programas de aplicación de la computadora.</p>
        <p><strong>Función:</strong> La función principal del OS es servir de intermediario entre el hardware (CPU, memoria, dispositivos I/O) y el usuario o los programas.</p>

        <h2>2. Componentes Clave de un OS</h2>
        <ul>
        <li><strong>Núcleo (Kernel):</strong> Componente central que gestiona la memoria, los procesos y la comunicación con el hardware.</li>
        <li><strong>Gestión de Procesos:</strong> Administra la creación, planificación, ejecución y terminación de los procesos (programas en ejecución).</li>
        <li><strong>Gestión de Memoria:</strong> Asigna y desasigna espacio en la memoria principal (RAM) para los programas y datos.</li>
        <li><strong>Gestión de Archivos:</strong> Organiza los datos en el almacenamiento (disco duro) mediante estructuras jerárquicas (directorios/carpetas).</li>
        <li><strong>Interfaz de Usuario:</strong> Permite la interacción con el usuario, puede ser una CLI (Línea de Comandos) o una GUI (Gráfica).</li>
        </ul>

        <h2>3. Ejemplos de Sistemas Operativos</h2>
        <p>Algunos de los sistemas operativos más comunes son:</p>
        <ul>
        <li>Windows</li>
        <li>macOS</li>
        <li>Linux</li>
        <li>Android</li>
        <li>iOS</li>
        </ul>
