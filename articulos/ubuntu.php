        <h1>Ubuntu Linux: El Sistema Operativo para la Humanidad</h1>
        <hr>

        <h2>1. Definición y Enfoque</h2>
        <p>
            Ubuntu es una de las distribuciones de GNU/Linux más populares a nivel mundial. Es desarrollado por **Canonical Ltd.**
        </p>
        <p>
            Su nombre proviene de la filosofía africana que significa "Humanidad hacia otros" y su objetivo principal es proporcionar un sistema operativo **fácil de usar**, estable y completamente gratuito para ordenadores de escritorio, servidores y dispositivos en la nube.
        </p>

        <h2>2. Características Distintivas</h2>
        <p>Ubuntu se ha convertido en una opción muy popular gracias a:</p>
        <ol>
            <li><strong>Base Debian:</strong> Está basado en la distribución Debian, conocida por su gran estabilidad, pero Ubuntu ofrece ciclos de lanzamiento más predecibles.</li>
            <li><strong>Fácil de Usar:</strong> Se enfoca en una experiencia de usuario amigable, lo que lo convierte en una excelente opción para principiantes en Linux.</li>
            <li><strong>LTS (Long-Term Support):</strong> Cada dos años, lanza una versión LTS que recibe soporte y actualizaciones de seguridad durante cinco años, ideal para empresas y usuarios que buscan estabilidad.</li>
            <li><strong>Entorno de Escritorio:</strong> Utiliza el entorno de escritorio **GNOME** (con modificaciones) por defecto, ofreciendo una interfaz moderna.</li>
            <li><strong>Amplia Compatibilidad:</strong> Tiene una gran comunidad y un excelente soporte de hardware y software, facilitando la instalación de drivers y programas populares.</li>
        </ol>

        <h2>3. Ediciones Principales</h2>
        <p>Ubuntu ofrece versiones adaptadas a diferentes entornos:</p>
        <ul>
            <li><strong>Ubuntu Desktop:</strong> La versión estándar para ordenadores personales.</li>
            <li><strong>Ubuntu Server:</strong> Una versión minimalista sin interfaz gráfica, optimizada para servidores y el alojamiento de aplicaciones web.</li>
            <li><strong>Ubuntu Core:</strong> Diseñado específicamente para dispositivos de Internet de las Cosas (IoT) y sistemas embebidos.</li>
            <li><strong>Sabores Oficiales (Flavours):</strong> Versiones que utilizan otros entornos de escritorio (como Kubuntu con KDE, Xubuntu con XFCE, etc.).</li>
        </ul>
