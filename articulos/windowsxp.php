        <h1>Microsoft Windows XP: El Sistema Operativo Duradero</h1>
        <hr>
    
        <h2>1. Datos Generales e Históricos</h2>
        <p>
            Windows XP fue un sistema operativo desarrollado por Microsoft y lanzado al mercado en **octubre de 2001**. Fue uno de los sistemas operativos más populares y duraderos de la historia de la informática personal.
        </p>
        <ul>
            <li><strong>Base:</strong> Utilizó el núcleo **Windows NT**, lo que le dio una estabilidad mucho mayor que sus predecesores orientados al consumidor (Windows 95, 98 y ME).</li>
            <li><strong>Fin de Soporte:</strong> Su soporte extendido finalizó en **abril de 2014**, aunque siguió siendo utilizado por muchos años después.</li>
        </ul>
    
        <h2>2. Características Destacadas</h2>
        <p>Windows XP introdujo varias novedades significativas:</p>
        <ol>
            <li><strong>Interfaz Gráfica (Luna):</strong> Presentó una nueva interfaz visual, conocida como "Luna", con esquinas redondeadas, sombras y un esquema de color más vibrante, lo que marcó un cambio respecto a la apariencia gris de Windows 2000.</li>
            <li><strong>Estabilidad Mejorada:</strong> Gracias a su base NT, era mucho menos propenso a fallos y errores críticos que la familia Windows 9x.</li>
            <li><strong>Restaurar Sistema:</strong> Incluyó la herramienta "Restaurar Sistema", que permitía a los usuarios revertir el sistema a un estado anterior para solucionar problemas de software.</li>
            <li><strong>Cambio Rápido de Usuario:</strong> Permitió a múltiples usuarios tener sesiones abiertas simultáneamente, facilitando el cambio entre cuentas sin cerrar aplicaciones.</li>
            <li><strong>Firewall Integrado:</strong> Introdujo un cortafuegos personal por defecto (aunque a menudo desactivado inicialmente) para aumentar la seguridad en la conexión a Internet.</li>
        </ol>
    
        <h2>3. Ediciones Principales</h2>
        <ul>
            <li><strong>Home Edition:</strong> Dirigida al usuario doméstico promedio.</li>
            <li><strong>Professional Edition:</strong> Incluía funciones adicionales para redes empresariales y entornos de negocio, como el soporte para múltiples procesadores y la encriptación de archivos.</li>
        </ul>
    
